public class P0038_Plus {
	public static void main(String[] args) {
		
		System.out.println(100 + 98);
		System.out.println("100" + 98);
		System.out.println(100 + 98 + "2");
		System.out.println("100" + 98 + 2);

		/*
			both are numeric then + means sum
			one is string then + means attach
			from left to right
		*/
	}
}