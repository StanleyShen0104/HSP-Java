public class P0029_DirDOS {
	public static void main(String[] args) {

		/*
			整体右移:	  	选中+Tab键
			整体左移: 		选中+Shift+Tab键
			相对路径:		从当前文件夹出发(..\ 跳向上一级)
			绝对路径:		从盘符根目录出发
		*/
		
		/*
			tree X:\...:	显示X下的全文件树状表
			dir X:\...:		显示该文件夹内的文件信息列表
			cd X:\...:		切换目录
			cd /D X:\...:	切换盘符及目录
			cd \:			回到盘符根目录
			cls:			cmd清屏
			md:				建立文件夹
			rd:				删除文件夹
			echo:			echo helloworld > hello.txt 创建包含内容的文件
			type:			type nul > hello.txt  创建空文件
			del: 			删除文件
			copy:			copy name.file X:\...  文件名可以重新定义，不一定要前后一致
			move:			move name.file X:\...  文件名可以重新定义，不一定要前后一致
		*/
	}
}